C ******************************************************************
C ORIGINAL JCL FOR IBM MAINFRAME
C //GO.FT04F001 DD UNIT=3400-4,COL=SER=NPS688,DISP=(OLD,KEEP),
C // DCB=(DEN=2,RECFM=FB,LRECL=80,BLKSIZE=3200),LABEL=(1,SL,,IN),
C // DSN=S2514.NDC2
C ******************************************************************
C
C
C                 ********** NODCDCUM ***************
C
C
C               SOURCE:  DR. R.G.  PACQUETTE
C
C
C         THIS PROGRAM PROVIDES A LISTING OF HEADERS FOR NPS688 AND
C         ASSIGNS A SERIAL NUMBER IN COLUMNS 81-85
C
C
      DIMENSION HDR1(9),HDR4(9)
      REWIND 4
      NREC=0
      NHD=0
      WRITE(6,10)
   10 FORMAT(1X,'LISTING OF HEADERS FOR NODC TAPE: NPS433',44X,'NHD',3X,
     1'NREC')
   11 READ(4,100,END=300)HDR1,HDR2,HDR3,HDR4,HDR5,ITYP
  100 FORMAT(9A4,A1,A4,9A4,A2,I1)
      NREC=NREC+1
      IF(ITYP.NE.1)GO TO 11
      NHD=NHD+1
      WRITE(6,200)HDR1,HDR2,HDR3,HDR4,HDR5,ITYP,NHD,NREC
  200 FORMAT(1X,9A4,A1,A4,9A4,A2,I1,2X,I5,2X,I6)
      GO TO 11
  300 WRITE(6,400)
  400 FORMAT(//5X,'END OF FILE')
  999 STOP
      END
